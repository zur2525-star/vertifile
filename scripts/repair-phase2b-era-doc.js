#!/usr/bin/env node
'use strict';

/**
 * Phase 2B-era doc repair — one-shot surgical fix.
 *
 * BACKGROUND
 *   Between Phase 2B (commit 491564a, 2026-04-07 02:23 UTC) and Phase 2C
 *   (commit 07648c0, 2026-04-07 14:02 UTC), there was an 11-hour window
 *   during which dual-signed docs got a frozen pvf_content whose init()
 *   function does NOT forward ed25519Signature/ed25519KeyId in the
 *   /api/verify fetch body. When such a doc is opened at /d/<shareId>,
 *   the init() runs, Phase 2C's no-downgrade rejects with
 *   reason='ed25519_required', and the page displays as FORGED.
 *
 *   Diagnostic query confirmed exactly ONE affected doc: VP6mXapK9bU.
 *   Half-state corruption check (COUNT(*) WHERE
 *   ed25519_signature IS NULL XOR ed25519_key_id IS NULL) returned 0.
 *
 * APPROACH
 *   Replace ONLY the <script> block inside the stored pvf_content with
 *   a fresh one generated by the current generatePvfHtml(). The file
 *   display body (outside the script) is preserved byte-for-byte. The
 *   Ed25519 signature is unaffected (it was signed over a payload with
 *   codeIntegrity: '' by design — see services/pvf-pipeline.js:222-253).
 *   The HMAC signature is unaffected (over the file hash only). The
 *   chain_token IS affected and is recomputed from the new codeIntegrity
 *   using the formula in services/pvf-pipeline.js:414-418.
 *
 * USAGE
 *   # Dry-run (default) — prints diff, writes nothing
 *   DATABASE_URL=... node scripts/repair-phase2b-era-doc.js --share-id=VP6mXapK9bU
 *
 *   # Wet mode — creates backup then writes
 *   DATABASE_URL=... node scripts/repair-phase2b-era-doc.js --share-id=VP6mXapK9bU --write
 *
 * POST-WET VERIFICATION
 *   1. Open https://vertifile.com/d/VP6mXapK9bU in a browser (or hit the
 *      staged env first if you have one).
 *   2. In devtools, eval: document.body.className — must NOT contain
 *      'forged'. Expected classes are 'verified' or '' (empty).
 *   3. Eval: document.body.getAttribute('data-fail-reason') — must be null.
 *   4. Network tab → /api/verify request body — confirm it includes
 *      ed25519Signature and ed25519KeyId fields. Response must be
 *      {verified: true, signedBy: 'both' (or equivalent)}.
 *
 * ROLLBACK
 *   The script writes a backup to /tmp/repair-backup-<shareId>-<ts>.json
 *   BEFORE any UPDATE. To roll back, read the backup file and run:
 *     UPDATE documents
 *        SET pvf_content = $1,
 *            code_integrity = $2,
 *            chained_token = $3
 *      WHERE hash = $4
 *   passing pvf_content_old, code_integrity_old, chained_token_old,
 *   and hash from the backup JSON.
 *
 * SAFETY INVARIANTS (do NOT weaken)
 *   - Dry-run is the default; --write is required for any mutation.
 *   - createdAt must be a string (doc.timestamp, no Date coercion). If
 *     the column ever returns a Date object this script aborts loudly.
 *   - Obfuscator fallback is detected by exact equality against the
 *     pre-obfuscation source — if they match, the worker fell back.
 *     The new script is also required to contain the hexadecimal
 *     identifier pattern _0x<hex>, which is an inescapable footprint
 *     of the javascript-obfuscator library with identifierNamesGenerator
 *     set to 'hexadecimal' (see obfuscate.js:25 / workers/obfuscate-worker.js).
 *   - Script splice uses lastIndexOf('<script>') / lastIndexOf('</script>'),
 *     not regex, to defend against obfuscated string-array entries that
 *     might contain literal '<script>' or '</script>' substrings.
 *   - Byte-identical check on the HTML body: stripping the <script> block
 *     from BOTH the original and the repaired pvf_content must yield
 *     identical strings. If they differ, the script aborts.
 *   - A backup file MUST exist on disk and be readable BEFORE the UPDATE
 *     is issued. Otherwise the script aborts.
 *   - Only pvf_content, code_integrity, and chained_token are UPDATEd.
 *     hash, signature, ed25519_signature, ed25519_key_id, created_at,
 *     org_id, share_id are NEVER touched.
 */

const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

const db = require(path.resolve(__dirname, '..', 'db.js'));
const { generatePvfHtml } = require(path.resolve(__dirname, '..', 'templates', 'pvf.js'));
const { obfuscatePvf } = require(path.resolve(__dirname, '..', 'obfuscate.js'));
const { HMAC_SECRET } = require(path.resolve(__dirname, '..', 'services', 'pvf-generator.js'));

// ----------------------------------------------------------------------
// Arg parsing — no external deps
// ----------------------------------------------------------------------
function parseArgs() {
  const out = { write: false, shareId: null };
  for (const a of process.argv.slice(2)) {
    if (a === '--write') out.write = true;
    else if (a.startsWith('--share-id=')) out.shareId = a.substring('--share-id='.length);
    else if (a === '--help' || a === '-h') {
      out.help = true;
    }
  }
  return out;
}

function printHelp() {
  process.stdout.write([
    'Usage: node scripts/repair-phase2b-era-doc.js --share-id=<shareId> [--write]',
    '',
    '  --share-id=<id>   (required) target document share_id',
    '  --write           perform the UPDATE (default is dry-run)',
    '  --help            print this message',
    '',
    'Environment:',
    '  DATABASE_URL      (required) postgres connection string',
    ''
  ].join('\n'));
}

// ----------------------------------------------------------------------
// <script> splice helpers — lastIndexOf for defense in depth
// ----------------------------------------------------------------------
function locateScriptRange(html, label) {
  const openIdx = html.lastIndexOf('<script>');
  const closeIdx = html.lastIndexOf('</script>');
  if (openIdx < 0 || closeIdx < 0 || closeIdx <= openIdx) {
    throw new Error('[' + label + '] could not locate <script>...</script> tags');
  }
  return { openIdx, closeIdx };
}

function stripScript(html, label) {
  const { openIdx, closeIdx } = locateScriptRange(html, label || 'stripScript');
  return html.substring(0, openIdx) + html.substring(closeIdx + '</script>'.length);
}

function extractScriptBlock(html, label) {
  const { openIdx, closeIdx } = locateScriptRange(html, label || 'extractScriptBlock');
  return html.substring(openIdx, closeIdx + '</script>'.length);
}

function extractScriptInner(html, label) {
  const block = extractScriptBlock(html, label);
  return block.substring('<script>'.length, block.length - '</script>'.length);
}

function sha256Hex(str) {
  return crypto.createHash('sha256').update(str).digest('hex');
}

// ----------------------------------------------------------------------
// Main
// ----------------------------------------------------------------------
async function main() {
  const args = parseArgs();
  if (args.help) { printHelp(); process.exit(0); }
  if (!args.shareId) {
    process.stderr.write('ERROR: --share-id=<shareId> is required\n\n');
    printHelp();
    process.exit(1);
  }
  if (!process.env.DATABASE_URL) {
    process.stderr.write('ERROR: DATABASE_URL must be set in the environment\n');
    process.exit(1);
  }

  const mode = args.write ? 'WET' : 'DRY-RUN';
  process.stdout.write('Repairing doc with share_id=' + args.shareId + '  mode=' + mode + '\n');

  await db._ready;

  // ---- Fetch doc via getDocumentByShareId → gives us hash + orgName + timestamp.
  // getDocumentByShareId returns a trimmed shape that does NOT include
  // code_integrity / chained_token / ed25519_* — we call getDocument(hash) next
  // to get those via mapDocRow.
  const docLite = await db.getDocumentByShareId(args.shareId);
  if (!docLite) {
    process.stderr.write('ERROR: no doc found with share_id=' + args.shareId + '\n');
    process.exit(1);
  }

  const doc = await db.getDocument(docLite.hash);
  if (!doc) {
    process.stderr.write('ERROR: getDocument(hash) returned null for hash=' + docLite.hash + '\n');
    process.exit(1);
  }

  // C2 — doc.timestamp must be a string (TEXT column per Phase 2C invariant).
  if (typeof doc.timestamp !== 'string') {
    process.stderr.write(
      'ERROR: doc.timestamp is not a string (got ' + typeof doc.timestamp + '). ' +
      'Schema drift detected — refusing to proceed.\n'
    );
    process.exit(1);
  }

  // Sanity: repair target MUST be dual-signed.
  if (!doc.ed25519_signature || !doc.ed25519_key_id) {
    process.stderr.write('ERROR: this doc is NOT dual-signed — not a valid repair target\n');
    process.exit(1);
  }

  // Pull pvf_content. db.getPvfContent takes a shareId, not a hash (see db.js:680).
  const originalPvfContent = await db.getPvfContent(args.shareId);
  if (!originalPvfContent || typeof originalPvfContent !== 'string') {
    process.stderr.write('ERROR: no pvf_content for this doc (share_id=' + args.shareId + ')\n');
    process.exit(1);
  }

  // Sanity: stored HTML must contain a <script> block.
  try {
    locateScriptRange(originalPvfContent, 'originalPvfContent');
  } catch (e) {
    process.stderr.write('ERROR: ' + e.message + '\n');
    process.exit(1);
  }

  // ---- Resolve orgName. Prefer doc.orgName (written at creation time in
  // services/pvf-pipeline.js:192 as `owner.displayName || 'VERTIFILE'`).
  // This is the byte-identical value passed into generatePvfHtml originally.
  // Fall back to the users table only if doc.orgName is missing (shouldn't
  // happen — documents.org_name is NOT NULL in the schema — but defense in depth).
  let orgName = doc.orgName;
  if (!orgName) {
    const rawOrgId = String(doc.orgId || '');
    if (rawOrgId.startsWith('user_')) {
      const userId = Number(rawOrgId.substring('user_'.length));
      if (Number.isFinite(userId)) {
        try {
          const user = await db.getUserById(userId);
          if (user) orgName = user.name || user.email || 'VERTIFILE';
        } catch (_) { /* fall through */ }
      }
    }
  }
  if (!orgName) orgName = 'VERTIFILE';

  // ---- Fetch branding (api_keys row). getBranding already returns a
  // default object if the org has no row.
  let branding;
  try {
    branding = await db.getBranding(doc.orgId);
  } catch (_) {
    branding = { custom_icon: null, brand_color: null, wave_color: null };
  }
  if (!branding) branding = { custom_icon: null, brand_color: null, wave_color: null };

  // ---- Generate fresh HTML with current generatePvfHtml. The fileBase64
  // placeholder is intentional: we only consume the <script> block from
  // this output. The script has no runtime dependency on the file body's
  // rendered content — it reads var HASH / var SIG / var SIG_ED / var KEY_ID
  // which the template bakes in from the params we pass.
  //
  // NOTE: the HTML BODY from this call is DISCARDED. The repaired pvf_content
  // is built by splicing into the STORED pvf_content (so the body stays
  // byte-identical to the original). C5 asserts this invariant.
  const freshFullHtml = generatePvfHtml(
    'REPAIR_PLACEHOLDER_FILE_BASE64',
    doc.originalName,
    doc.hash,
    doc.mimeType,
    doc.signature,
    doc.recipientHash || null,
    branding.custom_icon,
    branding.brand_color,
    orgName,
    doc.orgId,
    branding.wave_color,
    doc.shareId || args.shareId,
    doc.timestamp,                 // C2 — TEXT, byte-identical to DB, no Date() coercion
    doc.ed25519_signature,
    doc.ed25519_key_id
  );

  // Extract the fresh (UNOBFUSCATED) <script> block.
  const freshScriptBlockUnobf = extractScriptBlock(freshFullHtml, 'freshFullHtml');
  const freshScriptInnerUnobf = freshScriptBlockUnobf.substring(
    '<script>'.length,
    freshScriptBlockUnobf.length - '</script>'.length
  );

  // ---- Splice: replace the old (obfuscated) <script> block in the stored
  // HTML with the fresh (unobfuscated) one. We use the lastIndexOf-based
  // helpers, not a regex.
  const oldScriptBlock = extractScriptBlock(originalPvfContent, 'originalPvfContent');
  const { openIdx: spliceOpenIdx, closeIdx: spliceCloseIdx } =
    locateScriptRange(originalPvfContent, 'originalPvfContent:splice');

  const patchedHtml =
    originalPvfContent.substring(0, spliceOpenIdx) +
    freshScriptBlockUnobf +
    originalPvfContent.substring(spliceCloseIdx + '</script>'.length);

  if (patchedHtml === originalPvfContent) {
    process.stderr.write('ERROR: patched HTML equals original — splice did not occur\n');
    process.exit(1);
  }
  if (patchedHtml.length - originalPvfContent.length !==
      freshScriptBlockUnobf.length - oldScriptBlock.length) {
    process.stderr.write('ERROR: splice length accounting mismatch — aborting\n');
    process.exit(1);
  }

  // ---- Re-obfuscate the patched HTML. Use the SAME seed as the original
  // pipeline (see services/pvf-pipeline.js:392): first 8 hex chars of hash
  // as a 32-bit int.
  const seed = parseInt(doc.hash.substring(0, 8), 16);
  const reobfuscated = await obfuscatePvf(patchedHtml, seed);

  // ---- C3 — verify the obfuscator did NOT fall back to plain source.
  // The obfuscator's failure/timeout path resolves with the INPUT code
  // byte-for-byte (see obfuscate.js:65,74,80,87). Strongest possible check:
  // the new script inner MUST NOT equal the freshly generated unobfuscated
  // source. If they're equal, fallback definitely fired.
  const newScriptBlock = extractScriptBlock(reobfuscated, 'reobfuscated');
  const newScriptInner = newScriptBlock.substring(
    '<script>'.length,
    newScriptBlock.length - '</script>'.length
  );

  if (newScriptInner === freshScriptInnerUnobf) {
    process.stderr.write(
      'ERROR: obfuscator fallback detected — new script inner equals unobfuscated source.\n' +
      'This happens when the obfuscate worker times out, errors, or crashes.\n' +
      'See obfuscate.js:65/74/80/87. ABORTING — no writes performed.\n'
    );
    process.exit(1);
  }

  // Positive footprint check: javascript-obfuscator with
  // identifierNamesGenerator='hexadecimal' produces _0x[0-9a-f]+ identifiers
  // throughout the output. Require at least 5 matches as a sanity floor.
  const hexIdMatches = newScriptInner.match(/_0x[0-9a-fA-F]+/g) || [];
  if (hexIdMatches.length < 5) {
    process.stderr.write(
      'ERROR: obfuscator footprint missing — expected _0x<hex> identifiers ' +
      '(got ' + hexIdMatches.length + '). ABORTING — no writes performed.\n'
    );
    process.exit(1);
  }

  // (Previously a third "leading-comment" fallback check lived here, but the
  // comment it looked for never existed in templates/pvf.js. Removed per Zur's
  // review — the two checks above (exact equality vs unobfuscated source, plus
  // the _0x hex identifier footprint) are sufficient to detect a fallback.)

  // ---- Compute new codeIntegrity — sha256 of the obfuscated <script> INNER
  // body, matching services/pvf-pipeline.js:398-401. Note: the pipeline uses
  // regex capture group [1]; we use lastIndexOf. Both resolve to the inner
  // body of the single <script> block, which is byte-identical.
  const newCodeIntegrity = sha256Hex(newScriptInner);

  // ---- Compute new chain_token — matches services/pvf-pipeline.js:414-418.
  const newChainToken = crypto
    .createHmac('sha256', HMAC_SECRET)
    .update(doc.hash + doc.signature + doc.orgId + newCodeIntegrity)
    .digest('hex');

  // ---- C5 — byte-identical check on the HTML body.
  const oldBody = stripScript(originalPvfContent, 'originalPvfContent:strip');
  const newBody = stripScript(reobfuscated, 'reobfuscated:strip');
  if (oldBody !== newBody) {
    process.stderr.write(
      'ERROR: HTML body drifted during repair — ABORTING.\n' +
      '  old body length: ' + oldBody.length + '\n' +
      '  new body length: ' + newBody.length + '\n' +
      '  old sha256:      ' + sha256Hex(oldBody) + '\n' +
      '  new sha256:      ' + sha256Hex(newBody) + '\n'
    );
    process.exit(1);
  }

  // ---- Print the diff / dry-run report
  const oldScriptInner = extractScriptInner(originalPvfContent, 'originalPvfContent:innerReport');
  const oldPvfSha = sha256Hex(originalPvfContent);
  const newPvfSha = sha256Hex(reobfuscated);
  const bytesDelta = reobfuscated.length - originalPvfContent.length;

  process.stdout.write('\n=== Original state ===\n');
  process.stdout.write('  hash:             ' + doc.hash + '\n');
  process.stdout.write('  share_id:         ' + (doc.shareId || args.shareId) + '\n');
  process.stdout.write('  created_at:       ' + doc.timestamp + '\n');
  process.stdout.write('  org_id:           ' + doc.orgId + '\n');
  process.stdout.write('  org_name:         ' + (doc.orgName || '(null — using fallback "' + orgName + '")') + '\n');
  process.stdout.write('  mime_type:        ' + doc.mimeType + '\n');
  process.stdout.write('  original_name:    ' + doc.originalName + '\n');
  process.stdout.write('  ed25519_key_id:   ' + doc.ed25519_key_id + '\n');
  process.stdout.write('  pvf_content len:  ' + originalPvfContent.length + ' bytes\n');
  process.stdout.write('  pvf_content sha:  ' + oldPvfSha + '\n');
  process.stdout.write('  code_integrity:   ' + (doc.code_integrity || '(null)') + '\n');
  process.stdout.write('  chained_token:    ' + (doc.chained_token || '(null)') + '\n');
  process.stdout.write('  old script inner: ' + oldScriptInner.length + ' bytes\n');

  process.stdout.write('\n=== Repaired state (computed, not yet written) ===\n');
  process.stdout.write('  pvf_content len:  ' + reobfuscated.length + ' bytes (delta ' +
    (bytesDelta >= 0 ? '+' : '') + bytesDelta + ')\n');
  process.stdout.write('  pvf_content sha:  ' + newPvfSha + '\n');
  process.stdout.write('  code_integrity:   ' + newCodeIntegrity + '\n');
  process.stdout.write('  chained_token:    ' + newChainToken + '\n');
  process.stdout.write('  new script inner: ' + newScriptInner.length + ' bytes\n');
  process.stdout.write('  HTML body byte-identical: YES (verified via stripScript check)\n');

  process.stdout.write('\n=== Sanity checks ===\n');
  process.stdout.write('  obfuscator footprint (_0x hex identifiers): ' + hexIdMatches.length + ' matches\n');
  process.stdout.write('  obfuscator fallback detected:               NO\n');
  process.stdout.write('  new script starts with: ' + JSON.stringify(newScriptInner.substring(0, 200)) + '\n');

  if (!args.write) {
    process.stdout.write('\n=== DRY-RUN MODE — no writes performed ===\n');
    process.stdout.write('To apply the repair, re-run with --write\n');
    // Close the pool so the process exits cleanly.
    try { await db.close(); } catch (_) { /* ok */ }
    process.exit(0);
    return;
  }

  // ---- C7 — backup BEFORE the write
  const backupTs = new Date().toISOString().replace(/[:.]/g, '-');
  const backupPath = '/tmp/repair-backup-' + args.shareId + '-' + backupTs + '.json';
  const backupContent = JSON.stringify({
    hash: doc.hash,
    share_id: args.shareId,
    backup_at: new Date().toISOString(),
    pvf_content_old: originalPvfContent,
    code_integrity_old: doc.code_integrity || null,
    chained_token_old: doc.chained_token || null,
    // Metadata-only echoes for manual inspection. Never used by rollback.
    org_id: doc.orgId,
    ed25519_key_id: doc.ed25519_key_id,
    pvf_content_old_sha256: oldPvfSha
  }, null, 2);

  fs.writeFileSync(backupPath, backupContent);
  // Assert the backup file exists and is readable BEFORE any UPDATE.
  try {
    fs.accessSync(backupPath, fs.constants.R_OK);
    const verifyRead = fs.readFileSync(backupPath, 'utf-8');
    if (verifyRead.length !== backupContent.length) {
      throw new Error('backup read length mismatch (wrote ' + backupContent.length +
        ', read ' + verifyRead.length + ')');
    }
  } catch (e) {
    process.stderr.write('ERROR: backup verification failed: ' + e.message + ' — ABORTING before write\n');
    process.exit(1);
  }

  process.stdout.write('\nBackup written and verified: ' + backupPath + ' (' + backupContent.length + ' bytes)\n');

  // ---- Perform the UPDATE via the parameterized query helper. We update
  // ONLY the three columns documented in the header. The WHERE clause uses
  // the hash, which is immutable.
  const updateResult = await db.query(
    'UPDATE documents ' +
    '   SET pvf_content = $1, ' +
    '       code_integrity = $2, ' +
    '       chained_token = $3 ' +
    ' WHERE hash = $4',
    [reobfuscated, newCodeIntegrity, newChainToken, doc.hash]
  );

  const affected = updateResult && typeof updateResult.rowCount === 'number'
    ? updateResult.rowCount
    : '(unknown)';

  process.stdout.write('\n=== WRITE COMPLETE ===\n');
  process.stdout.write('  rows affected:  ' + affected + '\n');
  process.stdout.write('  hash:           ' + doc.hash.substring(0, 16) + '...\n');
  process.stdout.write('  share_id:       ' + args.shareId + '\n');
  process.stdout.write('  backup:         ' + backupPath + '\n');

  if (affected !== 1) {
    process.stderr.write(
      '\nWARNING: rowCount was ' + affected + ' (expected 1). ' +
      'Verify the repair manually before trusting the result.\n'
    );
  }

  process.stdout.write('\nNEXT: verify the repair by opening the doc in a browser:\n');
  process.stdout.write('  https://vertifile.com/d/' + args.shareId + '\n');
  process.stdout.write('Confirm body.className does NOT include "forged" and ' +
    'data-fail-reason is null (see POST-WET VERIFICATION in the script header).\n');

  try { await db.close(); } catch (_) { /* ok */ }
  process.exit(0);
}

main().catch((err) => {
  process.stderr.write('FATAL: ' + (err && err.message ? err.message : String(err)) + '\n');
  if (err && err.stack) process.stderr.write(err.stack + '\n');
  process.exit(1);
});
